
public class CincoTipoDeVariaveisNaTela {

	public static void main(String[] args) {
		
		int num = 5;
		double numb = 5.25;
		long loong = 4000000;
		short m = 10;
		float ff = 125;
		
		
		
		System.out.println("Int : " + num);
		System.out.println("Double : " + numb);
		System.out.println("long : " + loong);
		System.out.println("Short : " + m);
		System.out.println("Float : " + ff);

	}

}
