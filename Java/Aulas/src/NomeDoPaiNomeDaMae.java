import java.util.Scanner;

public class NomeDoPaiNomeDaMae {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner (System.in);

		System.out.println("Digite o nome de seu pai : ");
		String nomeDoPai = sc.next();
		
		System.out.println("Digite o nome da sua mãe : ");
		String nomeDaMae = sc.next();
		
		
		
		System.out.println("O nome do seu pai é : " + nomeDoPai );
		System.out.println("O nome da sua mãe é : " + nomeDaMae );
		
		
		
		
		
		
		
		
		
		
		
		
		
		sc.close();
	}

}
